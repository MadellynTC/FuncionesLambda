    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed
        try {
            // Obtener el texto ingresado en box_matriz
            String inputText = box_matriz.getText().trim();

            // Dividir el texto en líneas
            String[] lines = inputText.split("\n");

            // Crear una matriz para almacenar los valores
            int[][] matrix = new int[3][3];

            // Recorrer cada línea y dividir los elementos
            for (int i = 0; i < 3; i++) {
                String[] elements = lines[i].trim().split("\\s+");
                for (int j = 0; j < 3; j++) {
                    matrix[i][j] = Integer.parseInt(elements[j]);
                }
            }

            // Calcular el determinante
            int determinant = LambdaDeterminant.calculateDeterminant(matrix);

            // Mostrar el resultado en box_resultado
            box_resultado.setText(String.valueOf(determinant));
        } catch (Exception e) {
            box_resultado.setText("Error en el formato de entrada");
        }
        
    }//GEN-LAST:event_btn_calcularActionPerformed

